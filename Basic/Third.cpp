// Write a Program to Add Two Integer Numbers Entered by User

#include <bits/stdc++.h>
using namespace std;

int main()
{
    int num1, num2, sum;

    cout << "Enter First Number: " << endl;
    cin >> num1;

    cout << "Enter Second Number: " << endl;
    cin >> num2;

    sum = num1 + num2;

    cout << "The Sum is: " << sum << endl;

    return 0;
}