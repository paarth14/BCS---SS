// Write a Program to Multiply two decimal Numbersentered by User?

#include <bits/stdc++.h>
using namespace std;

int main()
{
    float num1, num2, mul;

    cout << "Enter First Number: " << endl;
    cin >> num1;

    cout << "Enter Second Number: " << endl;
    cin >> num2;

    mul = num1 * num2;

    cout << "Multiplication of " << num1 << " & " << num2 << " is: " << mul << endl;

    return 0;
}